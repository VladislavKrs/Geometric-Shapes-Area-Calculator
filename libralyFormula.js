export function square小alculation(side) {
    return side * side;
}

export function rectangle小alculation(sideA, sideB) {
    return sideA * sideB;
}

export function parallelogram小alculation(sideC, height) {
    return sideC * height
}

export function rhombus小alculation(sideD, height) {
    return sideD * height
}
